package java_basics;

public class literls {
    public static void main(String[] args) {
        // Literals in java

        int a = 10;
        System.out.println(a);

        int bin = 0B1100; // Taken a binary code 8421 format
        System.out.println(bin);

        int hexnum = 0x6E; //Hexa decimal value
        System.out.println(hexnum);

        int octalnum = 056;
        System.out.println(octalnum);

        int large_num = 7_98_00_000;
        System.out.println(large_num);

        double double_num = 216.43f;
        System.out.println(double_num);

        double exp_num = 92002.2342E2; 
        System.out.println(exp_num);
    }
}

